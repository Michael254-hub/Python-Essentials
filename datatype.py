# Data Types & Variables - Meet Python’s Building Blocks! 🧱

# Just like we have different types of stuff—like numbers 🧮, words 📖, and lists 📝—Python has data types to work with different kinds of information.

# Here are some key ones:

# int: Whole numbers (e.g., 3, 100)
# float: Decimal numbers (e.g., 3.14, 99.99)
# str: Text (e.g., "Hello, world!")
# bool: True or False (e.g., True, False)

# 💡 Variables are like containers where we store our data.

# 📝 Example:

# Variables hold data
age = 25          # int (integer)
height = 5.75      # float (decimal)
name = "Charlie"   # str (string)
is_student = True  # bool (boolean)

# Printing out the values
print("Name:", name)          # Outputs: Name: Charlie
print("Age:", age)            # Outputs: Age: 25
print("Height:", height)      # Outputs: Height: 5.75
print("Is a student?", is_student)  # Outputs: Is a student? True


# 💡 Tip: You can easily check a variable’s data type using the type() function:

print(type(age))       # Outputs: <class 'int'>
print(type(height))     # Outputs: <class 'float'>
print(type(name))         # Outputs: <class 'str'>